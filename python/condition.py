# Example 1: Simple if statement
number = 20
if number > 10:
    teks = "More than 10"
print(teks)

# Example 2: if-else statement
number2 = 20
if number2 < 10:
    teks2 = "Less than 10"
else:
    teks2 = "More than 10"
print(teks2)

# Example 3: if-elif-else statement
number3 = 10
if number3 < 10:
    teks3 = "Less than 10"
elif number3 < 20:
    teks3 = "More than 10"
else:
    teks3 = "Hello World"
print(teks3)
